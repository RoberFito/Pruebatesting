public class Person
{
    protected String dni;
    protected String mail;

    public Person() {
        dni = "";
        mail = "";
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = "";

        this.dni = dni;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = "";

        this.mail = mail;
    }
}